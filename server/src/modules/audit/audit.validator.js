import { z } from 'zod';

export const listAuditLogsQuerySchema = z.object({
  page: z.coerce.number().int().min(1).optional().default(1),
  limit: z.coerce.number().int().min(1).max(100).optional().default(50),
  action: z.enum([
    'user_banned',
    'user_unbanned',
    'user_role_changed',
    'template_approved',
    'template_rejected',
    'template_deleted',
    'review_moderated',
    'refund_approved',
    'refund_rejected',
    'coupon_created',
    'coupon_updated',
    'coupon_deleted',
    'creator_approved',
    'creator_rejected',
    'report_resolved',
    'report_dismissed',
    'withdrawal_approved',
    'withdrawal_rejected',
    'ticket_resolved',
    'system_config_changed',
  ]).optional(),
  userId: z.string().optional(),
  resource: z.enum([
    'user',
    'template',
    'review',
    'refund',
    'coupon',
    'creator',
    'report',
    'withdrawal',
    'ticket',
    'system',
  ]).optional(),
  dateFrom: z.coerce.date().optional(),
  dateTo: z.coerce.date().optional(),
});
